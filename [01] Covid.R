# Produzir s√©ries reais ---------------------------------------------------

Real <- function(x, y, final = FALSE) {
  # x     >> taxa de varia√ß√£o dos pre√ßos
  # y     >> s√©rie nominal
  # final >> trazer para pre√ßos do in√≠cio ou final do per√≠odo (padr√£o: in√≠cio)
  
  if (!is.vector(x) || !is.vector(y) || !(near(length(x),length(y))) || !is.logical(final) || length(final) > 1) {
    stop("checar entradas!")
  } else {
    if (final == FALSE) {
      indice    <- 1;
      for (i in 2:length(x)) {
        indice <- c(indice, last(indice)/(1+x[i]))
      }
    } else {
      indice <- 1; 
      for (i in (length(x)):2) {
        indice <- c(indice, (last(indice)*(1+x[i])))
      }
      indice <- rev(indice)  #inverter a ordem do vetor
    }  
    indice*y
  }
}


